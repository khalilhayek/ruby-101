# Now it's time to print some stuff.
# The simplest print statement is "puts". 
# It means "print string".
# puts prints strings, each followed by a new line.
# print prints strings not followed by a new line.
# Try these commands:

print 1, 2, 3
print "hello"

puts 1, 2, 3
puts "hello"

# Now try these:
print [1,2,3], "hello"
puts [1,2,3], "hello"



